package com.example.univibe.ui.theme

/**
 * Convenience typealias for accessing spacing constants.
 * Allows importing and using Spacing directly instead of Dimensions.Spacing
 * 
 * Usage:
 * ```
 * import com.example.univibe.ui.theme.Spacing
 * 
 * Box(modifier = Modifier.padding(Spacing.default))
 * ```
 */
typealias Spacing = Dimensions.Spacing